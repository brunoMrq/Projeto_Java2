package comando.estrutura.de.repeticao;

public class ComandoWhile {

	public static void main(String[] args) {
		
		int i = 0;
		
		while (i <= 10) {
		System.out.println("O número é: " + i);	
		i++;
		}

		for (int b = 10; b >= 0; b --) {
		System.out.println("O número é: " + (3*b));
		}
		
	}

}
